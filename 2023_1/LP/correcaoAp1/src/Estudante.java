public class Estudante {
    private String nome;
    private int idade;
    private int pontos;

    public String getNome(){
        return this.nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade(){
        return this.idade;
    }
    public void setIdade(int idade) {
        this.idade = idade;
    }

    public int getPontos(){
        return this.pontos;
    }
    public void setPontos(int pontos) {
        this.pontos = pontos;
    }
}
